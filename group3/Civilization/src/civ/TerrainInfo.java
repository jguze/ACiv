package civ;

import java.io.Serializable;

public class TerrainInfo implements Serializable{
    private int foodResource;
    private int productionResource;
    private int defenseBonus;
    private int movementCost;
    private float combatModifier;
    private int scienceResource;
    
    public TerrainInfo(int fR, int pR, int dB, int mC, float cM, int sR) {
        foodResource = fR;
        productionResource = pR;
        defenseBonus = dB;
        movementCost = mC;
        combatModifier = cM;
        scienceResource = sR;
    }

    public int getScienceResource() {
        return scienceResource;
    }

    public void setScienceResource(int scienceResource) {
        this.scienceResource = scienceResource;
    }
    
    public int getDefenseBonus() {
        return defenseBonus;
    }

    public void setDefenseBonus(int defenseBonus) {
        this.defenseBonus = defenseBonus;
    }

    public int getFoodResource() {
        return foodResource;
    }

    public void setFoodResource(int foodResource) {
        this.foodResource = foodResource;
    }

    public int getMovementCost() {
        return movementCost;
    }

    public void setMovementCost(int movementCost) {
        this.movementCost = movementCost;
    }

    public int getProductionResource() {
        return productionResource;
    }

    public void setProductionResource(int productionResource) {
        this.productionResource = productionResource;
    }

    public float getCombatModifier() {
        return combatModifier;
    }
}
